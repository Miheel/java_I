package lab_1;

public class Variables {

    public static void printVariables() {
        String myStr;
        int myInt;
        float myFloat;

        myStr = "Hello world";
        myInt = 10;
        myFloat = 34.5f;

        System.out.println("myStr: " + myStr);
        System.out.println("myInt: " + myInt);
        System.out.println("myFloat: " + myFloat);
    }
}
